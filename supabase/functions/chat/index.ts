import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are Ihumure AI, a compassionate and supportive mental health assistant for the Ihumure platform in Rwanda. Your role is to:

1. Provide emotional support and empathetic listening
2. Share evidence-based coping strategies for anxiety, depression, stress, and other mental health challenges
3. Guide users to appropriate resources and professional help when needed
4. Offer mindfulness and grounding techniques
5. Encourage self-care and healthy habits
6. Be culturally sensitive to Rwandan context

IMPORTANT GUIDELINES:
- Always be warm, non-judgmental, and supportive
- Never provide medical diagnoses or replace professional treatment
- If someone expresses suicidal thoughts or self-harm, immediately provide crisis resources
- Encourage users to seek professional help for serious concerns
- Keep responses concise but caring (2-3 paragraphs max)
- Use simple, accessible language
- Incorporate Rwandan cultural context when appropriate

Crisis Resources for Rwanda:
- Rwanda Mental Health Hotline: 114
- Police Emergency: 112
- Health Center: Visit your nearest health facility

Remember: You are here to support, not to diagnose or treat. Your goal is to help users feel heard, provide helpful coping strategies, and guide them toward professional resources when appropriate.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "I'm receiving too many requests right now. Please try again in a moment." }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }),
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "I'm having trouble connecting right now. Please try again." }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat function error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error occurred" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
